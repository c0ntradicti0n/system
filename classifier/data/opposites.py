def yield_random_hie_wordnet_sample():
    while True:
        with open("data/antonyms.csv", newline="") as csvfile:
            reader = csv.reader(csvfile)
            for row in reader:
                x = ast.literal_eval(row[1])
                r = random.randint(0, len(x) - 1)

                yield row[0], x[r]


